    public void ver_carrito(ListaEnlazada<Producto> c){
        DefaultListModel modelo = new DefaultListModel();
        jList1.setModel(modelo);
        
        for(int i = 0; i<c.getSize();i++){
            modelo.addElement(c.obtener(i).getNombre()+ ": ");
            modelo.addElement("$"+c.obtener(i).getPrecio()+" Marca: "+c.obtener(i).getMarca());
            modelo.addElement("Referencia: "+c.obtener(i).getReferencia()+" Id: "+c.obtener(i).getId());
            modelo.addElement(" ");
        }        
    }
    
    public void añadir_al_carrito(Producto i){
        carrito.add(i);  
    }
    
    
    public void limpiar(ListaEnlazada<Producto> c){
        DefaultListModel mod = new DefaultListModel();
        jList1.setModel(mod);
        
        mod.clear();
        mod.addElement("El carrito de compras está vacio");
        for(int i = 0; i<c.getSize();i++){
            c.eliminar(i);
        }
        
    }
    
    public void borrar(ListaEnlazada<Producto> c){
        
        if(c.getSize()==0){
            JOptionPane.showMessageDialog(null,"No hay productos añadidos al carrito.");
        }else{
            int id_borrar=Integer.parseInt(JOptionPane.showInputDialog("Ingrese el Id del producto que desea borrar"));
            for(int i = 0; i<c.getSize();i++){
                if(c.obtener(i).getId()==id_borrar){
                    c.eliminar(i);
                }
            }

            ver_carrito(carrito); 
            if(c.isEmpty()){
                DefaultListModel mod = new DefaultListModel();
                jList1.setModel(mod);
                mod.addElement("El carrito de compras está vacio");
            }
            
        }

    }
    
    public void comprar(ListaEnlazada<Producto> c){
        Recibo reci = new Recibo();
        
        if(c.getSize()==0){
            JOptionPane.showMessageDialog(null,"No hay productos añadidos al carrito.");
        }else{
            String direccion = JOptionPane.showInputDialog("Ingrese la direccion de envio");
            JOptionPane.showMessageDialog(null,"Compra realizada satisfactoriamente.");
        
            reci.ver_recibo(c, direccion);
        
            reci.setVisible(true);
            
            this.setVisible(false);
            
            limpiar(c);
        }
        
    }
    
    
