    public void registrar(){
        try{
                    String correo;
                    String contraseña,contraseña2;
                    String nombre;
                    correo = jTextField1.getText();
                    contraseña = jTextField2.getText();
                    contraseña2 = jTextField3.getText();
                    if(contraseña.equals(contraseña2)==false){
                       JOptionPane. showMessageDialog( null, "ERROR\nLas contraseñas no coinciden", 
                                                "Failure", JOptionPane. ERROR_MESSAGE);
                    }else{
                    nombre = jTextField4.getText();
                    File archivo= new File("usuario.txt");
                    FileWriter fw = new FileWriter(archivo.getAbsoluteFile(), true); 
                    PrintWriter pw = new PrintWriter(fw);
                    String cadena=correo+","+contraseña+","+nombre;
                    pw.append(cadena+"\n");
                    pw.flush();
                    pw.close();
                    JOptionPane.showMessageDialog(null, "Registro exitoso\nBienvenido: "+nombre);
                    UI_Main um= new UI_Main();
                    UI_Main.jLabel7.setText(nombre);
                    um.setVisible(true);
                    this.setVisible(false);
                    }
                }catch(Exception e){
                    JOptionPane. showMessageDialog( null, "ERROR\nNo se encontró la dirección del archivoS", 
                                                "Failure", JOptionPane. ERROR_MESSAGE);
                }
    }
