       public void  leer(){
            File archivo = new File("Datos10000.csv");
            long inicio = System.currentTimeMillis(),fin;
            double tiempo;
            Scanner flujoEntrada;
            String linea;
            String [] palabras ;
            Producto producto;
            String referencia;
            String nombre;
            int id;
            String marca;
            double precio;
            try{
            if (archivo.exists()){
               
                flujoEntrada = new Scanner (archivo);
                while(flujoEntrada.hasNextLine()){
                    linea = flujoEntrada.nextLine();
                    palabras = linea.split(";");
                    id = Integer.parseInt(palabras[0]);
                    nombre = palabras[1];
                    referencia = palabras[3];
                    precio = Double.parseDouble(palabras[4]); 
                    marca = palabras[2];                   
                    producto = new Producto(id,nombre,marca,referencia,precio);
                    productos.add(producto);
                    arbolProductos.insertar(id, producto);
                }
                fin = System.currentTimeMillis();
                tiempo = (double)((fin-inicio)/1000);
                System.out.println("Tiempo en leer y cargar en memoria los productos:"+tiempo
                                        +"segundos");
            }
            }catch(Exception e){
                System.out.println(e);
                JOptionPane.showMessageDialog(null, "Error en el archivo");
            }
        }
    
    public void venta(){
        if(productos.getSize()== 0){
           leer(); 
        }else{
            
              try{
                    String nombre = jTextField1.getText();
                    String marca = jTextField3.getText();
                    double precio = Double.parseDouble(jTextField2.getText());
                    String referencia = jTextField4.getText();
                    FileWriter fw = new FileWriter("Datos10000.csv",true);
                    System.out.println(productos.getSize());
                    Producto prod = new Producto(productos.getSize()+1,nombre,marca, referencia,precio);
                    productos.add(prod);
                    PrintWriter pw = new PrintWriter(fw);
                    pw.print("\n"+prod.getId()+";"+prod.getNombre()+";"+prod.getMarca()+";"+prod.getReferencia()+";"+prod.getPrecio());
                    pw.flush();
                    pw.close();
                    fw.close();
                    JOptionPane.showMessageDialog(null, "El producto ha sido vendido. El Id asignado es: "+ prod.getId());
            
            }catch(Exception e){
                System.out.println("No se encontr√≥ el archivo o error en el ingreso de un archivo ");
                System.out.println(e);
            }      
            
        } 
    }
    
